package chapters.chapter05.chapter05checkpoints;

public class CheckPoint5_08 {
	public static void main(String[] args) {
		
		a();
		b();
		
		
		
	}
	public static void a() {
		int sum = 0;
		for (int i = 0; i < 10; ++i) {
			sum += i;
			}
		System.out.println(sum);
	}
	public static void b() {
		int sum = 0;
		for (int i = 0; i < 10; i++) {
			sum += i;
			}
		System.out.println(sum);
		
	}
}
